package ubicrypt.core.provider.ftp;

import ubicrypt.core.dto.ProviderConf;

public class FTPConf extends ProviderConf {
  private String host;
  private int port = -1;
  private String folder;

  public String getHost() {
    return host;
  }

  public void setHost(String host) {
    this.host = host;
  }

  public int getPort() {
    return port;
  }

  public void setPort(int port) {
    this.port = port;
  }

  public String getFolder() {
    return folder;
  }

  public void setFolder(String folder) {
    this.folder = folder;
  }
}
